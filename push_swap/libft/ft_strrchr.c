#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if (s[i] == (t_uc)c)
			return ((char *)s + i);
		i--;
	}
	if ((t_uc)c == '\0')
		return ((char *)s);
	return (NULL);
}
